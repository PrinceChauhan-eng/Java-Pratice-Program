package PracticeSet7;

class Pattern {
    void pattern() {
        int n = 4;
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }

    }

    public static void main(String[] args) {
        Pattern p = new Pattern();
        p.pattern();
    }
}
